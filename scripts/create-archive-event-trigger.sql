
DELIMITER $$

CREATE EVENT RequestArchiveEvent1
  ON SHEDULE
      EVERY 1 DAY
      STARTS (TIMESTAMP(CURRENT_DATE) + INTERVAL 21 HOUR)
  ON COMPLETION PRESERVE
DO
BEGIN
  CALL CreateArchiveRequests(60);
  CALL CreateArchiveRequestsWithInvalidDate(240);
  CALL CreateArchiveFiles();
END$$

